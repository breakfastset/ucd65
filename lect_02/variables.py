# 4 basic types
# text, integer, float, boolean
name = "Yo yo"           # string
age = 23                 # integer
height = 1.83            # float
has_graduated = False    # boolean
print(name, type(name))
print(age, type(age))
print(height, type(height))
print(has_graduated, type(has_graduated))
print("--" * 30)

# -----------------------
# Identifier name rules
# ----------------------
# 1. Can only contain alphabets (A-Z, a-z), underscores _ and digits (0 - 9)
# 2. Must only start with alphabets or underscores
# 3. Cannot be keywords (if, else, for, while, class etc)
#
class_name = "class 2025"

# ---------------------------
# Variable naming guidelines
# ---------------------------
# 1. Must be meaningful
# 2. Must all be in lower case
# 3. Use underscores to link words
# 4. Variables are descriptive like properties
#
num_chairs = 40
average_weight = 23.45
height_in_metres = 1.83    # better than height sometimes




